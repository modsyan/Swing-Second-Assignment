    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String path = "";
        JFileChooser pdf = new JFileChooser();
        pdf.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = pdf.showSaveDialog(this);
        if (x == JFileChooser.APPROVE_OPTION){
            path = pdf.getSelectedFile().getPath();
        }
        Document doc = new Document();
        try {
            
            PdfWriter writer = PdfWriter.getInstance(doc, new FileOutputStream(path  +"\\Data.pdf"));
            doc.open();
            Paragraph Name = new Paragraph("Name : "+UserNamePrintFiled.getText());
            Paragraph Email = new Paragraph("Email : "+EmailPrintFiled.getText());
            Paragraph Gender = new Paragraph("Gender : "+GenderPrintFiled.getText());
            Paragraph Country = new Paragraph("Country : "+CountryFiled.getText());
            Paragraph Birthday = new Paragraph("Birthday : "+BDPrintFiled.getText());
            Paragraph Question = new Paragraph("Question : "+QuestionPrintFiled.getText());
            Paragraph Answer = new Paragraph("Answer : "+AnswerPrintFiled2.getText());
            
            
            doc.add(Name);
            doc.add(Email);
            doc.add(Gender);
            doc.add(Country);
            doc.add(Birthday);
            doc.add(Question);
            doc.add(Answer);
            
            doc.close();
            System.out.println("Done Saved");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


